package trigonometric_taylor_series;

@SuppressWarnings("serial")
public class Factorial_Exception extends Exception {

	public Factorial_Exception() {
		super();
		
	}

	public Factorial_Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public Factorial_Exception(String message, Throwable cause) {
		super(message, cause);
		
	}

	public Factorial_Exception(String message) {
		super(message);
		
	}

	public Factorial_Exception(Throwable cause) {
		super(cause);
	}

	

}
